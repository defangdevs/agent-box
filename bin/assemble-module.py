#!/usr/bin/env python3
"""Assemble modules/agent-box.nix from modules/agent-box.nix.in + modules/src/*.

Why this exists
---------------
Deployed boxes fetch EXACTLY ONE file — aws/template.yaml's user-data and
agent-box-update.service both `builtins.fetchurl .../modules/agent-box.nix` and
pin a single sha256 (issue #51). raw.githubusercontent is dual-stack (IPv6-only
boxes reach it via NAT64) and needs no unpack step on a journal-only first boot.
That constraint is on the *fetched artifact*, not on how we author it.

So we keep the source split into real, tool-visible assets (a .py the linters
can see, later .js/.css) and mechanically re-embed them into the one committed
file at build time. `modules/agent-box.nix` is GENERATED; edit the `.in`
template and `modules/src/`, then rerun this script (or `nix run .#assemble`).

Include mechanism
-----------------
A line matching `<indent>@@include:<relpath>@@` (relative to the including
file's directory) is replaced by the target file's contents:
  * nested includes in the target are resolved first;
  * the target is escaped for the STRING SYNTAX of the including file — Nix
    indented-string escaping when the host is a `.nix`/`.in` file, none
    otherwise (Python triple-quote hosts store their assets verbatim; the
    round-trip guarded by the up-to-date CI check catches an asset needing more);
  * every non-blank line is re-indented to the marker's own indentation
    (blank lines stay empty), so a dedented, col-0 source file (real Python the
    linters accept) round-trips back to its indented in-string form byte-exact.
"""
import argparse
import difflib
import re
import sys
from pathlib import Path

MARKER = re.compile(r"^(?P<indent>[ ]*)@@include:(?P<path>[^@]+)@@[ ]*$")

BANNER = """\
# @@GENERATED@@ — DO NOT EDIT THIS FILE BY HAND.
# Source of truth: modules/agent-box.nix.in + modules/src/*. Regenerate with
#   nix run .#assemble        (or: python3 bin/assemble-module.py)
# CI (`module-generated-up-to-date`) fails if this file is stale.
"""


def nix_escape(text: str) -> str:
    """Escape a literal for embedding inside a Nix '' indented string.

    Only `''` and `${` are special there (backslashes are literal). Order
    matters: escape `''` first, then `${` — the `''${` we emit for `${` must
    not be re-processed by the `''` rule."""
    text = text.replace("''", "'''")
    text = text.replace("${", "''${")
    return text


def escaper_for(path: Path):
    if path.suffix in (".nix", ".in"):
        return nix_escape
    return lambda s: s


def reindent(text: str, indent: str) -> str:
    return "\n".join(indent + line if line else line for line in text.split("\n"))


def resolve(path: Path) -> str:
    """Return `path` with all its @@include@@ markers expanded.

    Children are escaped for THIS file's string syntax and re-indented to the
    marker column; `path`'s own bytes are returned as-is."""
    escape = escaper_for(path)
    out = []
    for line in path.read_text().split("\n"):
        m = MARKER.match(line)
        if not m:
            out.append(line)
            continue
        child = (path.parent / m.group("path")).resolve()
        if not child.exists():
            sys.exit(f"assemble-module: {path}: missing include target {child}")
        payload = resolve(child)
        # A source file's conventional terminal newline is not part of the
        # in-string payload — drop exactly one so the marker line is replaced
        # in place rather than gaining a trailing blank line.
        if payload.endswith("\n"):
            payload = payload[:-1]
        out.append(reindent(escape(payload), m.group("indent")))
    return "\n".join(out)


def assemble(repo: Path) -> str:
    template = repo / "modules" / "agent-box.nix.in"
    return BANNER + resolve(template)


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__)
    ap.add_argument(
        "--check",
        action="store_true",
        help="exit non-zero (with a diff) if the committed file is stale",
    )
    ap.add_argument(
        "--repo",
        type=Path,
        default=Path(__file__).resolve().parent.parent,
        help="repo root (default: parent of bin/)",
    )
    args = ap.parse_args()
    out_path = args.repo / "modules" / "agent-box.nix"
    generated = assemble(args.repo)
    if args.check:
        current = out_path.read_text() if out_path.exists() else ""
        if current != generated:
            diff = difflib.unified_diff(
                current.splitlines(keepends=True),
                generated.splitlines(keepends=True),
                fromfile="modules/agent-box.nix (committed)",
                tofile="modules/agent-box.nix (regenerated)",
            )
            sys.stderr.writelines(diff)
            sys.stderr.write(
                "\nmodules/agent-box.nix is stale — run `nix run .#assemble`.\n"
            )
            return 1
        print("modules/agent-box.nix is up to date")
        return 0
    out_path.write_text(generated)
    print(f"wrote {out_path}")
    return 0


if __name__ == "__main__":
    sys.exit(main())
